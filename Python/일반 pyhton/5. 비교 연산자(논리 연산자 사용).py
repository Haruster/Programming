#   논리 자료형 : true혹은 false를 나타내는 자료형을 논리 자료형이라고 한다. (논리 자료형은 딱 true, false 두가지 타입이 있다.)
#   비교 연산자 : 논리 연산자를 활용한 것으로 숫자나 값을 비교하는 연산자이다. 주어진 진술이 참이면 true 틀리면 false를 출력한다.

print(3 < 5) #false가 출력됨
print(5 > 3) #true가 출력됨
print(5 == 3) #true가 출력됨
print(2 >= 10) #false가 출력됨
print(5 >= 10) #false가 출력됨
print(5 != 10) #true가 출력됨

